import React, { useEffect, useState } from "react";
import axios from "axios";

const Students = () => {
    const [students, setStudents] = useState([]);
    const [sortOrder, setSortOrder] = useState("asc"); // asc | desc

    // G·ªçi API l·∫•y danh s√°ch sinh vi√™n
    useEffect(() => {
        fetchStudents();
    }, []);

    const fetchStudents = async () => {
        try {
            const response = await axios.get("http://localhost:8080/students");
            setStudents(response.data);
        } catch (error) {
            console.error("L·ªói khi l·∫•y danh s√°ch sinh vi√™n:", error);
        }
    };

    // S·∫Øp x·∫øp danh s√°ch theo t√™n
    const handleSortByName = () => {
        const sorted = [...students].sort((a, b) => {
            if (sortOrder === "asc") {
                return a.name.localeCompare(b.name);
            } else {
                return b.name.localeCompare(a.name);
            }
        });
        setStudents(sorted);
        setSortOrder(sortOrder === "asc" ? "desc" : "asc");
    };

    return (
        <div style={{ padding: "40px", textAlign: "center" }}>
            <h2>üìö Trang Qu·∫£n l√Ω Sinh vi√™n</h2>
            <div style={{ overflowX: "auto", marginTop: "30px" }}>
                <table style={styles.table}>
                    <thead>
                    <tr>
                        <th>ID</th>
                        <th>MSSV</th>
                        <th>
                            T√™n{" "}
                            <button onClick={handleSortByName} style={styles.sortButton}>
                                {sortOrder === "asc" ? "‚Üë" : "‚Üì"}
                            </button>
                        </th>
                        <th>Ng√†y sinh</th>
                        <th>L·ªõp</th>
                        <th>Khoa</th>
                        <th>SƒêT</th>
                        <th>Email</th>
                    </tr>
                    </thead>
                    <tbody>
                    {students.length > 0 ? (
                        students.map((s, index) => (
                            <tr key={s.studentId || index}>
                                <td>{s.studentId}</td>
                                <td>{s.studentCode}</td>
                                <td>{s.name}</td>
                                <td>{s.dateOfBirth}</td>
                                <td>{s.gradeId}</td>
                                <td>{s.facultyId}</td>
                                <td>{s.phone}</td>
                                <td>{s.email}</td>
                            </tr>
                        ))
                    ) : (
                        <tr>
                            <td colSpan="8" style={{ padding: "20px" }}>
                                Kh√¥ng c√≥ d·ªØ li·ªáu sinh vi√™n.
                            </td>
                        </tr>
                    )}
                    </tbody>
                </table>
            </div>
        </div>
    );
};

// CSS n·ªôi tuy·∫øn
const styles = {
    table: {
        width: "100%",
        borderCollapse: "collapse",
        backgroundColor: "#fff",
        boxShadow: "0 2px 6px rgba(0,0,0,0.1)",
    },
    sortButton: {
        background: "none",
        border: "none",
        cursor: "pointer",
        fontSize: "16px",
        marginLeft: "5px",
    },
};

export default Students;
